%Function to return the value for shape function (vector) at the         % 
%"xi" and "eta" in the reference space.                                  %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%Author : Ashutosh                                                        %
%On : 29th June, 2017                                                     %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


function [N] = ShapeFunction (xi,eta)

N = [(1 - xi)*(1 - eta)/4; ...
    (1 + xi)*(1 - eta)/4; ...
    (1 + xi)*(1 + eta)/4; ...
    (1 - xi)*(1 + eta)/4];

end